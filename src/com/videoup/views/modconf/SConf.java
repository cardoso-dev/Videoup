    @Override
    public void loadEntity(Object oEnt){
        ent=((VideoupConf)oEnt);
        txNomClub.setText(ent.getNVclub());
        txDNIDef.setText(ent.getDniDef());
        txDNIRegExp.setText(ent.getDniRgexp());
        txPostCodeRegExp.setText(ent.getPcodeRgexp());
        jtaCond.setDocumentText(ent.getContrato());
        jcbMaxOfrt.setSelectedItem(""+ent.getMaxoffrts());
        txtPgTam.setText(""+ent.getPagesize());
        txtMinExtra.setText(""+ent.getFracMinExtra());
        jckChangEstr.setSelected(ent.getChangeFinEstreno());
        updateLogo();
        changes=false;
    }
    
