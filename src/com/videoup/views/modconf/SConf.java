    private void btnSelectLogoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectLogoActionPerformed
        byte[] blob=GenProccess.getImageBlob(ctrl.getMainWindow(),"Elegir imagen");
        if(blob==null){
            ctrl.showDialogErr(GenProccess.getError());
        }else if(blob.length>0){
            ent.setLogoVclub(blob);
            changes=true;
            updateLogo();
        }
    }//GEN-LAST:event_btnSelectLogoActionPerformed
    
    private void updateLogo(){
        InputStream inpstr;
        BufferedImage bfImg;
        if(ent.getLogoVclub()==null){
            return;
        }
        inpstr=new ByteArrayInputStream(ent.getLogoVclub());
        try {
            bfImg = ImageIO.read(inpstr);
        }catch (IOException ex) {
            lblImgError.setText("Error: "+ex.getMessage());
            pnlLogo.add(lblImgError,BorderLayout.CENTER);
            return;
        }
        if(imgLogo!=null){
            pnlLogo.remove(imgLogo);
        }
        imgLogo=new ImagePanel(bfImg,new Dimension(143,189));
        pnlLogo.add(imgLogo,BorderLayout.CENTER);
        validate();
    }
    
