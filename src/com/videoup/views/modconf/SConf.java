    @Override
    protected boolean saveEntity(VideoupBaseEntity ignore,boolean forceAsNew){
        boolean saved;
        ent.setNVclub(txNomClub.getText());
        ent.setDniDef(txDNIDef.getText());
        ent.setDniRgexp(txDNIRegExp.getText());
        ent.setPcodeRgexp(txPostCodeRegExp.getText());
        ent.setContrato(jtaCond.getDocumentBody());
        ent.setMaxoffrts( Integer.parseInt(jcbMaxOfrt.getSelectedItem().toString()) );
        ent.setPagesize( Integer.parseInt(txtPgTam.getText()) );
        ent.setFracMinExtra( Integer.parseInt(txtMinExtra.getText()) );
        ent.setChangeFinEstreno(jckChangEstr.isSelected());
        saved=super.saveEntity(ent,forceAsNew);
        changes=!saved;
        return saved;
    }
    
